package sample.DataStructure;

public class CharInput {
    private char col_0;
    private int col_1;

    public CharInput(char col_0, int col_1) {
        this.col_0 = col_0;
        this.col_1 = col_1;
    }

    public char getCol_0() {
        return col_0;
    }

    public void setCol_0(char col_0) {
        this.col_0 = col_0;
    }

    public int getCol_1() {
        return col_1;
    }

    public void setCol_1(int col_1) {
        this.col_1 = col_1;
    }
}
